<?php

$n = 35;
if($n>36){
    echo "Нет 4-значного числа, сумма цифр которого будет равна " . $n;
}else{
    for($i=1000; $i<=9999; $i++){
        $sum = 0;
        for($j = $i; $j>0; $j/=10){
            $j = floor($j);
            $sum+=($j%10);
        }
        if($sum == $n){
            echo $i . "</br>";
        }
    }
}
