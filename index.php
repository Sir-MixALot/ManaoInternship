$N = 2195;
$M = 0;
echo 'Ваше число: ' . $N . '</br>';
if(floor($N/10)==0) {
    echo "Введите число больше одной цифры";
}else{
    for($i = 1; $i < 10; $i++)
    {
        for($j=$N; $j>0; $j=floor($j/10)){
            if($j%10==$i){
                $M = ($M*10)+$i;
            }
        }
    }
    echo $M;
}


